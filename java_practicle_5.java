// java code for displaying Multiplication Table Of Fiftin
public class java_practicle_5 {
    public static void main(String args[]){
        int i=1;
        int M;


        System.out.println("Multiplication table of 15 is :");

        while(i<=10){
            M =15*i;
            System.out.println("15 x "+i +"= "+M);
            i = i+1 ;
        }
    }
}
